    private void initTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tblLoaiAo.getModel();
        String[] columns = new String[]{
            "Mã Thương hiệu", "Tên thương hiệu", "Trạng thái"
        };

        tableModel.setColumnIdentifiers(columns);
    }

    private void fillTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tblLoaiAo.getModel();
        tableModel.setRowCount(0);
        List<LoaiAo> list = ladao.selectAll();
        for (LoaiAo la : list) {
            tableModel.addRow(new Object[]{
                la.getMaLoaiAo(), la.getTenLoaiAo(), la.isTrangThai() ? "Đang hoạt động" : "Ngừng hoạt động"
            });
        }
        tblLoaiAo.setModel(tableModel);
    }

    private void initComboBox() {
        DefaultComboBoxModel boxModel = (DefaultComboBoxModel) cboTrangThai.getModel();
        boxModel.removeAllElements();
        String[] cboItem = new String[]{
            "Đang hoạt động", "Ngừng hoạt động"
        };
        cboTrangThai.setModel(new DefaultComboBoxModel<>(cboItem));
    }

    private LoaiAo getModel() {
        return new LoaiAo(0, txtTenLoaiAo.getText(), cboTrangThai.getSelectedIndex() == 0);
    }

    private boolean validateForm() {
        String tenLoaiAo = txtTenLoaiAo.getText().trim();
        if (tenLoaiAo.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên loại áo không được để trống .");
            return false;
        }
        return true;
    }

    private void clear() {
        txtMaLoaiAo.setText("");
        txtTenLoaiAo.setText("");
        cboTrangThai.setSelectedIndex(0);
        row = -1;
    }

