    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            if (ladao.KiemTraTenLoaiAo(txtTenLoaiAo.getText())) {
                if (ladao.insert(getModel()) > 0) {
                    JOptionPane.showMessageDialog(this, "Thêm loại áo thành công .");
                    fillTable();
                    clear();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm loại áo không thành công .");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Tên loại áo đã tồn tại trong bảng .");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if (row > -1) {
            if (validateForm()) {
                LoaiAo loaiAo = getModel();
                loaiAo.setMaLoaiAo(Integer.parseInt(txtMaLoaiAo.getText().trim()));
                if (ladao.update(loaiAo) > 0) {
                    JOptionPane.showMessageDialog(this, "Cập nhật loại áo thành công .");
                    fillTable();
                    clear();
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật loại áo không thành công . ");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa dòng nào được chọn trên bảng .");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed

        if (row > -1) {
            LoaiAo loaiAo = getModel();
            loaiAo.setMaLoaiAo(Integer.parseInt(txtMaLoaiAo.getText().trim()));
            loaiAo.setTrangThai(false);
            if (ladao.update(loaiAo) > 0) {
                JOptionPane.showMessageDialog(this, "Xóa loại áo thành công .");
                fillTable();
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa loại áo không thành công . ");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa dòng nào được chọn trên bảng .");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblLoaiAoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLoaiAoMouseClicked
        // TODO add your handling code here:
        row = tblLoaiAo.getSelectedRow();
        txtMaLoaiAo.setText(tblLoaiAo.getValueAt(row, 0).toString());
        txtTenLoaiAo.setText(tblLoaiAo.getValueAt(row, 1).toString());
        if (tblLoaiAo.getValueAt(row, 2).toString().equals("Đang hoạt động")) {
            cboTrangThai.setSelectedIndex(0);
        } else {
            cboTrangThai.setSelectedIndex(1);
        }
    }//GEN-LAST:event_tblLoaiAoMouseClicked

