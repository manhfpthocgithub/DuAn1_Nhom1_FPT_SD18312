    public boolean PhanQuyenNV() {
        String tenDN = txtTenDangNhap.getText();
        String mk = new String(txtMatKhau.getPassword());
        NguoiDung nd = ndDAO.selectById(tenDN);
        if (tenDN.isEmpty()) {
            MsgBox.alert(this, "Chưa Điền Tên Đăng Nhập!");
            return false ;
        } else if (mk.isEmpty()) {
            MsgBox.alert(this, "Chưa Điền Mật Khẩu!");
            return false ;
        } else if (nd == null) {
            MsgBox.alert(this, "Sai Tên Đăng Nhập!");
            return false ;
        } else if (!nd.getMatKhau().equals(mk)) {
            MsgBox.alert(this, "Sai Mật Khẩu!");
            return false ;
        } else {
            Auth.user = nd;
            MsgBox.alert(this, "Đăng Nhập Thành Công!");
            if (nd.isVaiTro() == true) {
                JOptionPane.showMessageDialog(this, "Bạn đang đăng nhập bằng quyền quản lí...");
            } else {
                JOptionPane.showMessageDialog(this, "Bạn đang đăng nhập bằng quyền nhân viên...");
            }
        }
        return true;
    }

    //neu isVaiTro() = false thi la nguoi quan li
    //neu isVaiTro() = true thi la nhan vien
    void DangNhap() {
        if (PhanQuyenNV()) {
            Main main = new Main();
            main.setVisible(true);
            this.dispose();
        }
    }

    void ketthuc() {
        int id = JOptionPane.showConfirmDialog(this, "Bạn muốn thoát?", "Thông Báo!", JOptionPane.YES_OPTION);
        if (id == 0) {
            System.exit(0);
        }
    }

