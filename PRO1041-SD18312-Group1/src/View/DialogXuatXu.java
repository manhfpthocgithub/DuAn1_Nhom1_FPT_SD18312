    private void initTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tblXuatXu.getModel();
        String[] columns = new String[]{
            "Mã Thương hiệu", "Tên thương hiệu", "Trạng thái"
        };

        tableModel.setColumnIdentifiers(columns);
    }

    private void fillTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tblXuatXu.getModel();
        tableModel.setRowCount(0);
        List<XuatXu> list = xxdao.selectAll();
        for (XuatXu xx : list) {
            tableModel.addRow(new Object[]{
                xx.getMaXuatXu(), xx.getXuatXu(), xx.isTrangThai() ? "Đang hoạt động" : "Ngừng hoạt động"
            });
        }
        tblXuatXu.setModel(tableModel);
    }

    private void initComboBox() {
        DefaultComboBoxModel boxModel = (DefaultComboBoxModel) cboTrangThai.getModel();
        boxModel.removeAllElements();
        String[] cboItem = new String[]{
            "Đang hoạt động", "Ngừng hoạt động"
        };
        cboTrangThai.setModel(new DefaultComboBoxModel<>(cboItem));
    }

    private XuatXu getModel() {
        return new XuatXu(0,txtTenXuatXu.getText(), cboTrangThai.getSelectedIndex() == 0);
    }

    private boolean validateForm() {
        String tenXuatXu = txtTenXuatXu.getText().trim();
        if (tenXuatXu.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên xuất xứ không được để trống .");
            return false;
        }

        return true;
    }

    private void clear() {
        txtMaXuatXu.setText("");
        txtTenXuatXu.setText("");
        cboTrangThai.setSelectedIndex(0);
        row = -1 ;
    }

