    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            if (xxdao.KiemTraTenXuatXu(txtTenXuatXu.getText())) {
                if (xxdao.insert(getModel()) > 0) {
                    JOptionPane.showMessageDialog(this, "Thêm xuất xứ thành công .");
                    fillTable();
                    clear();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm xuất xứ không thành công .");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Tên thương hiệu đã tồn tại trong bảng .");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if (row > -1) {
            if (validateForm()) {
                XuatXu xuatXu = getModel();
                xuatXu.setMaXuatXu(Integer.parseInt(txtMaXuatXu.getText().trim()));
                if (xxdao.update(xuatXu) > 0) {
                    JOptionPane.showMessageDialog(this, "Cập nhật xuất xứ thành công .");
                    fillTable();
                    clear();
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật xuất xứ không thành công . ");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa dòng nào được chọn .");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        System.out.println(row);
        if (row > -1) {
            XuatXu xuatXu = getModel();
            xuatXu.setMaXuatXu(Integer.parseInt(txtMaXuatXu.getText().trim()));
            xuatXu.setTrangThai(false);
            if (xxdao.update(xuatXu) > 0) {
                JOptionPane.showMessageDialog(this, "Xóa xuất xứ thành công .");
                fillTable();
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa xuất xứ không thành công . ");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa dòng nào được chọn .");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblXuatXuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblXuatXuMouseClicked
        // TODO add your handling code here:
        row = tblXuatXu.getSelectedRow();
        txtMaXuatXu.setText(tblXuatXu.getValueAt(row, 0).toString());
        txtTenXuatXu.setText(tblXuatXu.getValueAt(row, 1).toString());
        if (tblXuatXu.getValueAt(row, 2).toString().equals("Đang hoạt động")) {
            cboTrangThai.setSelectedIndex(0);
        } else {
            cboTrangThai.setSelectedIndex(1);
        }
    }//GEN-LAST:event_tblXuatXuMouseClicked

