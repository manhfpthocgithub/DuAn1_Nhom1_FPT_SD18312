    private void initTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tblMauSac.getModel();
        String[] columns = new String[]{
            "Mã màu", "Tên màu", "Trạng thái"
        };

        tableModel.setColumnIdentifiers(columns);
    }

    private void fillTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tblMauSac.getModel();
        tableModel.setRowCount(0);
        List<MauSac> list = msdao.selectAll();
        System.out.println(list.get(1).getMaMau());
        for (MauSac ms : list) {
            tableModel.addRow(new Object[]{
                ms.getMaMau(), ms.getTenMauSac(), ms.isTrangThai() ? "Đang hoạt động" : "Ngừng hoạt động"
            });
        }
        tblMauSac.setModel(tableModel);
    }

    private void initComboBox() {
        DefaultComboBoxModel boxModel = (DefaultComboBoxModel) cboTrangThai.getModel();
        boxModel.removeAllElements();
        String[] cboItem = new String[]{
            "Đang hoạt động", "Ngừng hoạt động"
        };
        cboTrangThai.setModel(new DefaultComboBoxModel<>(cboItem));
    }

    private Color getColorFromString(String colorString) {
        try {
            return (Color) Color.class.getField(colorString.toLowerCase()).get(null);
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }

    private MauSac getModel() {
        return new MauSac(0, txtTenMauSac.getText(), cboTrangThai.getSelectedIndex() == 0);
    }

    private boolean validateForm() {
        String tenMauSac = txtTenMauSac.getText().trim();
        if (tenMauSac.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên màu sắc không được để trống .");
            return false;
        }
        return true;
    }

    private void clear() {
        txtMaMau.setText("");
        txtTenMauSac.setText("");
        cboTrangThai.setSelectedIndex(0);
        row = -1;
    }

