    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            if (msdao.KiemTraTenMau(txtTenMauSac.getText().trim())) {
                if (msdao.insert(getModel()) > 0) {
                    JOptionPane.showMessageDialog(this, "Thêm màu sắc thành công .");
                    fillTable();
                    clear();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm màu sắc mới không thành công .");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Màu đã có trong cơ sở dữ liệu");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void tblMauSacMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMauSacMouseClicked
        // TODO add your handling code here:
        row = tblMauSac.getSelectedRow();
        txtMaMau.setText(tblMauSac.getValueAt(row, 0).toString());
        txtTenMauSac.setText(tblMauSac.getValueAt(row, 1).toString());
        if (tblMauSac.getValueAt(row, 2).toString().equals("Đang hoạt động")) {
            cboTrangThai.setSelectedIndex(0);
        } else {
            cboTrangThai.setSelectedIndex(1);
        }
    }//GEN-LAST:event_tblMauSacMouseClicked

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        if (row > -1) {
            if (validateForm()) {
                MauSac mauSac = getModel();
                mauSac.setMaMau(Integer.parseInt(txtMaMau.getText().trim()));
                if (msdao.update(mauSac) > 0) {
                    JOptionPane.showMessageDialog(this, "Cập nhật màu sắc thành công .");
                    fillTable();
                    clear();
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật màu sắc không thành công .");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa dòng nào được chọn .");
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:

        if (row > -1) {
            MauSac mauSac = getModel();
            mauSac.setMaMau(Integer.parseInt(txtMaMau.getText().trim()));
            mauSac.setTrangThai(false);
            if (msdao.update(mauSac) > 0) {
                JOptionPane.showMessageDialog(this, "Xóa màu sắc thành công .");
                fillTable();
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa màu sắc không thành công .");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa dòng nào được chọn .");
        }


    }//GEN-LAST:event_btnXoaActionPerformed

