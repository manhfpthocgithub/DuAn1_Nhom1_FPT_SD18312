    private void initTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tblSize.getModel();
        String[] columns = new String[]{
            "Mã size", "Tên size", "Trạng thái"
        };

        tableModel.setColumnIdentifiers(columns);
    }

    private void fillTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tblSize.getModel();
        tableModel.setRowCount(0);
        List<Size> list = sizeDAO.selectAll();
        for (Size sz : list) {
            tableModel.addRow(new Object[]{
                sz.getMaSize(), sz.getTenSize(), sz.isTrangThai() ? "Đang hoạt động" : "Ngừng hoạt động"
            });
        }
        tblSize.setModel(tableModel);
    }

    private void initComboBox() {
        DefaultComboBoxModel boxModel = (DefaultComboBoxModel) cboTrangThai.getModel();
        boxModel.removeAllElements();
        String[] cboItem = new String[]{
            "Đang hoạt động", "Ngừng hoạt động"
        };
        cboTrangThai.setModel(new DefaultComboBoxModel<>(cboItem));
    }

    private Size getModel() {
        return new Size(0, txtTenSize.getText(), cboTrangThai.getSelectedIndex() == 0);
    }

    private boolean validateForm() {
        String tenSize = txtTenSize.getText().trim();
        if (tenSize.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên size không được để trống .");
            return false;
        }
        return true;
    }

    private void clear() {
        txtMaSize.setText("");
        txtTenSize.setText("");
        cboTrangThai.setSelectedIndex(0);
        row = -1 ;
    }
