    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            if (sizeDAO.KiemTraTenSize(txtTenSize.getText())) {
                if (sizeDAO.insert(getModel()) > 0) {
                    JOptionPane.showMessageDialog(this, "Thêm size thành công .");
                    fillTable();
                    clear();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm size không thành công .");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Tên size đã tồn tại trong bảng .");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if (row < -1) {
            if (validateForm()) {
                Size size = getModel();
                size.setMaSize(Integer.parseInt(txtMaSize.getText().trim()));
                if (sizeDAO.update(size) > 0) {
                    JOptionPane.showMessageDialog(this, "Cập nhật size thành công .");
                    fillTable();
                    clear();
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật size không thành công . ");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa dòng nào được chọn .");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed

        if (row < -1) {
            Size size = getModel();
            size.setMaSize(Integer.parseInt(txtMaSize.getText().trim()));
            size.setTrangThai(false);
            if (sizeDAO.insert(size) > 0) {
                JOptionPane.showMessageDialog(this, "Xóa size thành công .");
                fillTable();
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa size không thành công . ");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa dòng nào được chọn .");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblSizeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSizeMouseClicked
        // TODO add your handling code here:
        row = tblSize.getSelectedRow();
        txtMaSize.setText(tblSize.getValueAt(row, 0).toString());
        txtTenSize.setText(tblSize.getValueAt(row, 1).toString());
        if (tblSize.getValueAt(row, 2).toString() == "Đang hoạt động") {
            cboTrangThai.setSelectedIndex(0);
        } else {
            cboTrangThai.setSelectedIndex(1);
        }
    }//GEN-LAST:event_tblSizeMouseClicked

