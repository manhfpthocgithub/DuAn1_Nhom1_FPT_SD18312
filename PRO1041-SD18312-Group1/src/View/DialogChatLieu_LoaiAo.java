    private void initTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tblChatLieuLoaiAo.getModel();
        String[] columns = new String[]{
            "Mã chất liệu loại áo", "Tên chất liệu", "Tên loại áo", "Trạng thái"
        };
        tableModel.setColumnIdentifiers(columns);

    }

    private void fillTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tblChatLieuLoaiAo.getModel();
        tableModel.setRowCount(0);
        List<ChatLieu_LoaiAo> list = clladao.selectAll();
        for (ChatLieu_LoaiAo clla : list) {
            tableModel.addRow(new Object[]{
                clla.getMaChatLieuLoaiAo(), cldao.selectById(clla.getMaChatLieu()), ladao.selectById(clla.getMaLoaiAo()), clla.isTrangThai() ? "Đang hoạt động" : "Ngừng hoạt động"
            });
        }
        tblChatLieuLoaiAo.setModel(tableModel);
    }

    private void initComboboxChatLieu() {
        DefaultComboBoxModel boxModel = (DefaultComboBoxModel) cboChatLieu.getModel();
        boxModel.removeAllElements();
        List<ChatLieu> list = cldao.selectAll();
        for (ChatLieu cl : list) {
            boxModel.addElement(cl);
        }

        cboChatLieu.setModel(boxModel);
    }

    private void initComboboxLoaiAo() {
        DefaultComboBoxModel boxModel = (DefaultComboBoxModel) cboLoaiAo.getModel();
        boxModel.removeAllElements();
        List<LoaiAo> list = ladao.selectAll();
        for (LoaiAo la : list) {
            boxModel.addElement(la);
        }
        cboLoaiAo.setModel(boxModel);
    }

    private void initComboboxTrangThai() {
        DefaultComboBoxModel boxModel = (DefaultComboBoxModel) cboTrangThai.getModel();
        boxModel.removeAllElements();
        String[] cboList = new String[]{
            "Đang hoạt động", "Ngừng họat động"
        };
        for (String tt : cboList) {
            boxModel.addElement(tt);
        }
        cboTrangThai.setModel(boxModel);
    }

    private ChatLieu_LoaiAo getModel() {
        LoaiAo la = (LoaiAo) cboLoaiAo.getSelectedItem();
        ChatLieu cl = (ChatLieu) cboChatLieu.getSelectedItem();
        return new ChatLieu_LoaiAo(0, la.getMaLoaiAo(), cl.getMaChatLieu(), cboTrangThai.getSelectedItem() == "Đang hoạt động" ? true : false);
    }

    private void clear() {
        txtMaCLLA.setText("");
        cboChatLieu.setSelectedIndex(0);
        cboLoaiAo.setSelectedIndex(0);
        cboTrangThai.setSelectedIndex(0);
        row = -1;
    }

