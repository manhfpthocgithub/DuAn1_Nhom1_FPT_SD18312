    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        ChatLieu cl = (ChatLieu) cboChatLieu.getSelectedItem();
        LoaiAo la = (LoaiAo) cboLoaiAo.getSelectedItem();
        if (clladao.getChatLieuLoaiAo(cl.getMaChatLieu(), la.getMaLoaiAo()) == null) {
            if (clladao.insert(getModel()) > 0) {
                JOptionPane.showMessageDialog(this, "Thêm mới chất liệu loại áo thành công .");
                fillTable();
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm mới chất liệu loại áo không thành công .");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chất liệu của loại áo đã có trong cơ sở dữ liệu .");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:

        if (row > -1) {
            ChatLieu_LoaiAo chatLieu_LoaiAo = getModel();
            System.out.println(chatLieu_LoaiAo.isTrangThai());
            System.out.println(chatLieu_LoaiAo.getMaChatLieu());
            System.out.println(chatLieu_LoaiAo.getMaLoaiAo());
            chatLieu_LoaiAo.setMaChatLieuLoaiAo(Integer.parseInt(txtMaCLLA.getText().trim()));
            System.out.println(chatLieu_LoaiAo.getMaChatLieuLoaiAo());
            if (clladao.update(chatLieu_LoaiAo) > 0) {
                JOptionPane.showMessageDialog(this, "Cập nhật chất liệu loại áo thành công .");
                fillTable();
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật chất liệu loại áo không thành công .");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa dòng nào trên bảng được chọn .");
        }

    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed

        if (row > -1) {
            ChatLieu_LoaiAo clla = getModel();
            clla.setMaChatLieuLoaiAo(Integer.parseInt(txtMaCLLA.getText().trim()));
            clla.setTrangThai(false);
            System.out.println(clla.getMaChatLieu());
            System.out.println(clla.getMaLoaiAo());
            if (clladao.update(clla) > 0) {
                JOptionPane.showMessageDialog(this, "Xóa chất liệu loại áo thành công .");
                fillTable();
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa chất liệu loại áo không thành công .");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chưa dòng nào trên bảng được chọn .");
        }

    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblChatLieuLoaiAoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblChatLieuLoaiAoMouseClicked
        // TODO add your handling code here:
        row = tblChatLieuLoaiAo.getSelectedRow();
        txtMaCLLA.setText(tblChatLieuLoaiAo.getValueAt(row, 0).toString());
        Object selectedCl = tblChatLieuLoaiAo.getValueAt(row, 1);
        DefaultComboBoxModel<ChatLieu> cbBoxModel1 = (DefaultComboBoxModel) cboChatLieu.getModel();
        for (int i = 0; i < cbBoxModel1.getSize(); i++) {
            ChatLieu chatlieu = cbBoxModel1.getElementAt(i);
            if (chatlieu.toString().equals(selectedCl.toString())) {
                cboChatLieu.setSelectedItem(chatlieu);
            }
        }
        Object selectedLa = tblChatLieuLoaiAo.getValueAt(row, 2);
        DefaultComboBoxModel<LoaiAo> cbBoxModel2 = (DefaultComboBoxModel) cboLoaiAo.getModel();
        for (int i = 0; i < cbBoxModel2.getSize(); i++) {
            LoaiAo loaiAo = cbBoxModel2.getElementAt(i);
            if (loaiAo.toString().equals(selectedCl.toString())) {
                cboLoaiAo.setSelectedItem(loaiAo);
            }
        }
        if (tblChatLieuLoaiAo.getValueAt(row, 3).toString().equals("Đang hoạt động")) {
            cboTrangThai.setSelectedIndex(0);
        } else {
            cboTrangThai.setSelectedIndex(1);
        }
    }//GEN-LAST:event_tblChatLieuLoaiAoMouseClicked

